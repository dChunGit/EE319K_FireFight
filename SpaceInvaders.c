void Move1(){
	char1.prex=char1.x;
	char1.prey=char1.y;
	
	int data=GPIO_PORTB_DATA_R&0x07;
	
	if(char1.y<=(85-chesth[cheststate])||(data&0x02)==0x02||((data&0x01)==0x01&&char1.current==1))
	{
		if(((char1.y-char1.h[char1.current])>=30)||(char1.x>76)||((char1.x+char1.w[char1.current])<48)||((data&0x01)==0x01&&char1.current!=1)||((data&0x02)==0x02&&char1.current==1))
		{
			if((data&0x01)==0x01&&char1.current==0&&(char1.x-8)>0) //Facing front
				char1.x-=1;
			if((data&0x02)==0x02&&char1.current==0&&(char1.x+char1.w[char1.current]+8)<128)
				char1.x+=1;
			if((data&0x01)==0x01&&char1.current==2&&(char1.x+char1.w[char1.current]+8)<128) //Facing back
				char1.x+=1;
			if((data&0x02)==0x02&&char1.current==2&&(char1.x-8)>0)
				char1.x-=1;
			if((data&0x01)==0x01&&char1.current==1&&(char1.y-char1.h[char1.current]-8)>0) //Facing left
				char1.y-=1;
			if((data&0x02)==0x02&&char1.current==1&&(char1.y+8)<80)
				char1.y+=1;
			if(((data&0x01)==0x01)&&(char1.current==5)&&((char1.x-8)>0)&&((char1.y+8)<128)) //Facing rightplus
			{
				char1.x-=1;
				char1.y+=1;
			}
			if((data&0x02)==0x02&&(char1.current==5)&&((char1.x+char1.w[char1.current]+8)<128)&&((char1.y-char1.h[char1.current]-8)>0))
			{
				char1.x+=1;
				char1.y-=1;
			}
			if((data&0x01)==0x01&&(char1.current==4)&&(char1.y+8)<80) //Facing right
				char1.y+=1;
			if((data&0x02)==0x02&&(char1.current==4)&&(char1.y-char1.h[char1.current]-8)>0)
				char1.y-=1;
			if((data&0x01)==0x01&&(char1.current==3)&&(char1.x+char1.w[char1.current]+8)<128&&(char1.y+8)<80) //Facing rightminus
			{
				char1.x+=1;
				char1.y+=1;
			}
			if((data&0x02)==0x02&&(char1.current==3)&&(char1.x-8)>0&&(char1.y-char1.h[char1.current]-8)>0)
			{
				char1.x-=1;
				char1.y-=1;
			}
		}
	}
}

