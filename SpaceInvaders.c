void Rotate1(int direction){
	direction=direction/800; //ADC Input
	char1.preferred=direction;
	
	if(char1.current<char1.preferred)
		direction=1;
	else if(char1.current>char1.preferred)
		direction=-1;
	else direction=0;
	
	char1.prevc=char1.current;
	if(direction>0)
	{
		char1.current=(char1.current+1)%6;
	}else if(direction<0)
	{
		char1.current=(char1.current-1);
		if(char1.current<0)
			char1.current=5;
	}
}

